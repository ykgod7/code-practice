'''
1. permutation : 순서 상관 O, 원소들의 모든 순열을 나타냄
ex) list = [1, 2, 3]
==> permutations (list, 2)
==> [(1, 2), (1, 3), (2, 1), (2, 3), (3, 1), (3, 2)]


2. combination : 순서 상관 X, 원소들의 모든 조합을 나타냄
ex) list = [1, 2, 3]
==> combinations(list, 2)
==> [(1, 2), (1, 3), (2, 3)]


3. product : 순서 상관 O, 뽑은 수를 다시 리스트에 포함하여 순열을 나타냄
ex) list = [1, 2, 3]
==> product(list, repeat=2)
==> [(1, 1), (1, 2), (1, 3), (2, 2), (2, 1), (2, 3), (3, 3), (3, 1), (3, 2)]
'''
